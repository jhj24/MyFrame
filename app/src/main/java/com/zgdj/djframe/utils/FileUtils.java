    /**
     * 获取SD卡路径
     **/
    public static String getSDPath() {
        String sdcardPath = null;
        boolean sdCardExist = Environment.getExternalStorageState().equals(
                android.os.Environment.MEDIA_MOUNTED);  //判断sd卡是否存在
        if (sdCardExist) {
            sdcardPath = Environment.getExternalStorageDirectory().getAbsolutePath();  //获取根目录
        }
        if (sdcardPath != null) {
            return sdcardPath;
        } else {
            return "";
        }
    }

    public static String getSDPath(String subDir) {
        if (Environment.MEDIA_MOUNTED.equals(Environment.getExternalStorageState())) {
            String path = Environment.getExternalStorageDirectory().getAbsolutePath();
            if (!path.endsWith("/")) {
                path += "/";
            }
            path += PROJECT_DIR + "/";
            if (subDir != null && subDir.trim().length() > 0) {
                path += subDir + "/";
            }
            File file = new File(path);
            if (!file.exists()) {
                if (file.mkdirs()) {
                    return path;
                } else {
                    return null;
                }
            } else {
                if (file.isFile()) {
                    if ((file.delete())) {
                        if (file.mkdirs()) {
                            return path;
                        } else {
                            return null;
                        }
                    } else {
                        return null;
                    }
                } else {
                    return path;
                }
            }
        }
        return null;
    }

    public static void downLoad(String path, Context context) throws Exception {
        URL url = new URL(path);
        InputStream is = url.openStream();
        //截取最后的文件名
        String end = path.substring(path.lastIndexOf("."));
        //打开手机对应的输出流,输出到文件中
        OutputStream os = context.openFileOutput("Cache_" + System.currentTimeMillis() + end, Context.MODE_PRIVATE);
        byte[] buffer = new byte[1024];
        int len = 0;
        //从输入六中读取数据,读到缓冲区中
        while ((len = is.read(buffer)) > 0) {
            os.write(buffer, 0, len);
        }
        //关闭输入输出流
        is.close();
        os.close();
    }

    /**
     * Try to return the absolute file path from the given Uri
     *
     * @param context
     * @param uri
     * @return the file path or null
     */
    public static String getRealFilePath(final Context context, final Uri uri) {
        if (null == uri) return null;
        final String scheme = uri.getScheme();
        String data = null;
        if (scheme == null)
            data = uri.getPath();
        else if (ContentResolver.SCHEME_FILE.equals(scheme)) {
            data = uri.getPath();
        } else if (ContentResolver.SCHEME_CONTENT.equals(scheme)) {
            Cursor cursor = context.getContentResolver().query(uri, new String[]{MediaStore.Images.ImageColumns.DATA}, null, null, null);
            if (null != cursor) {
                if (cursor.moveToFirst()) {
                    int index = cursor.getColumnIndex(MediaStore.Images.ImageColumns.DATA);
                    if (index > -1) {
                        data = cursor.getString(index);
                    }
                }
                cursor.close();
            }
        }
        return data;
    }

